package confluo.rpc;

/**
 * Contains the primitive types
 */
public class TypeId {
    /** NONE: The none type */
    static rpc_data_type NONE = rpc_data_type.RPC_NONE;
    /** BOOL: The boolean type */
    static rpc_data_type BOOL = rpc_data_type.RPC_BOOL;
    /** CHAR: The character type */
    static rpc_data_type CHAR = rpc_data_type.RPC_CHAR;
    /** UCHAR: The unsigned character type */
    static rpc_data_type UCHAR = rpc_data_type.RPC_UCHAR;
    /** SHORT: The short type */
    static rpc_data_type SHORT = rpc_data_type.RPC_SHORT;
    /** USHORT: The unsigned short type */
    static rpc_data_type USHORT = rpc_data_type.RPC_USHORT;
    static rpc_data_type INT = rpc_data_type.RPC_INT;
    static rpc_data_type UINT = rpc_data_type.RPC_UINT;
    static rpc_data_type LONG = rpc_data_type.RPC_LONG;
    static rpc_data_type ULONG = rpc_data_type.RPC_ULONG;
    static rpc_data_type FLOAT = rpc_data_type.RPC_FLOAT;
    static rpc_data_type DOUBLE = rpc_data_type.RPC_DOUBLE;
    static rpc_data_type STRING = rpc_data_type.RPC_STRING;
}
